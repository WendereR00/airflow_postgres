GRANT SELECT ON TABLE case_one.employees TO pols1 WITH GRANT OPTION;
GRANT SELECT ON TABLE case_one.employees_hours TO pols1 WITH GRANT OPTION;
GRANT SELECT ON TABLE case_one.positions TO pols1 WITH GRANT OPTION;
GRANT SELECT ON TABLE case_one.working_phones TO pols1 WITH GRANT OPTION;


